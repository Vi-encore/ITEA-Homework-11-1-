function isAge() {
  let age = +prompt(`–°–∫—ñ–ª—å–∫–∏ –í–∞–º —Ä–æ–∫—ñ–≤?`);
  if (!Number(age)) {
    alert(`Enter correct age value (number)`);
  } else {
    let submitAge = confirm(`–í–∞–º ${age} —Ä–æ–∫–∏(-—ñ–≤)?`);
    if (!submitAge) {
      alert(`–í–∞–º –ù–ï ${age} —Ä–æ–∫–∏(-—ñ–≤)!!! üò¢`); //false
    } else {
      alert(`–í–∞–º ${age} —Ä–æ–∫–∏(-—ñ–≤) üòÑ`); //true
    }
  }
}

